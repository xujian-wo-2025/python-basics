my_str = "itheima and itcast"
print(my_str[2])   # h

# index方法
print(my_str.index('and'))   # 8

# replace方法
print(my_str.replace('it', '程序')) # 程序heima and 程序cast

# split方法
my_str = "hello python itheima itcast"
print(my_str.split(' ')) # ['hello', 'python', 'itheima', 'itcast']

# strip方法
my_str = "     itheima is itcast "
print(my_str.strip())   # itheima is itcast

